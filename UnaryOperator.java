
public class UnaryOperator {

	public static void main(String[] args) {
		
		int result =+1;
		System.out.println("Result is :"+result);
		
		result--;
		System.out.println("result is :"+result);
		
		result++;
		System.out.println("result is "+result);
		
		result= -result;
		System.out.println("result is :"+result);
		
		boolean success=false;
		System.out.println("result is :"+ success);
		System.out.println("result is :"+!success);

	}

}
